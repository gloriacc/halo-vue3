<template>
  <div class="nav">
    <div class="logo" @click="toggleAside">LOGO</div>
    <ul class="menu">
      <li>菜单1</li>
      <li>菜单2</li>
    </ul>
  </div>
</template>
<script lang="ts">
  import {inject, Ref} from 'vue';

  export default {
    name: 'Nav',
    setup () {
      const asideVisible = inject<Ref<Boolean>>('asideVisible')
      const toggleAside = () => {
        if (asideVisible) {
          asideVisible.value = !asideVisible.value
        }
      }
      return {toggleAside}
    }
  }
</script>
